% --- Executes on button press in pushbutton7.
function pushbutton7_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton7 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
popup_sel_index = get(handles.popupmenu2, 'Value');
tic
MWF_framework(popup_sel_index);
runtime = toc;

[speechSig,Fs]= audioread('speech_channel3.wav');
[outputSig,Fs]=audioread('output.wav');
outputSig=[outputSig;zeros(length(speechSig)-length(outputSig),1)];
d = stoi(speechSig,outputSig(1:(0.5*length(outputSig))),Fs);

% noise estimation
N_fft = 512;                        % number of FFT points
R_fft = N_fft/2;                    % shifting (50% overlap)
win = sqrt(hann(N_fft,'periodic')); % analysis window
[noiseMat, SPP] = spp_calc(outputSig,N_fft,R_fft);
noise = calc_ISTFT(noiseMat, win, N_fft, N_fft/R_fft, 'onesided');

SNR = snr(outputSig,noise);
textLabel = sprintf('STOI value = %0.3f ',d);
set(handles.text1, 'String', textLabel);
textLabel = sprintf('SNR (processed signal) = %0.3f ',SNR);
set(handles.text2, 'String', textLabel);
textLabel = sprintf('Runtime = %0.3f s',runtime);
set(handles.text3, 'String', textLabel);

guidata(hObject, handles);

msgbox('Running Completed');
